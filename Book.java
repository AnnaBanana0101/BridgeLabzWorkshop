
public class Book {

    public String title;
    public String author;
    public String publisher;
    public String genre;
    public int no_of_pages;

    public Book(String title, String author, String publisher, String genre, int no_of_pages)
    {
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.genre = genre;
        this.no_of_pages = no_of_pages;
    }

}